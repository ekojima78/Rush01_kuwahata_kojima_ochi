void	parse_input(char *str, int constraints[16])
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (str[i] && j < 16)
	{
		if (str[i] >= '1' && str[i] <= '4')
		{
			constraints[j] = str[i] - '0';
			j++;
		}
		i++;
	}
}
